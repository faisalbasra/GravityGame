package se.liu.tdp021.gravityGame;


public abstract class DynamicGameObject extends GameObject {
	public abstract void update();
	
	public abstract void setPosition(float x, float y, boolean setPrev);
	public abstract void setCollisionX(float x);
	public abstract void setCollisionY(float y);
	
	public abstract float getXVelocity();
	public abstract float getYVelocity();
	public abstract void setXVelocity(float vel);
	public abstract void setYVelocity(float vel);
	
	public abstract void setOnGroundState(boolean state);
	public abstract float getXPrev();
	public abstract float getYPrev();
}
